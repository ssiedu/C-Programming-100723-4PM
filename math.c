#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n ceil function : %.2f",ceil(3.2));
	printf("\n ceil function : %.2f",ceil(3.9));
	
	printf("\n floor function : %.2f",floor(3.2));
	printf("\n floor function : %.2f",floor(3.2));
	
	printf("\n sqrt function : %.2f",sqrt(81));
	printf("\n sqrt function : %.2f",sqrt(7));
	
	printf("\n pow function : %.2f",pow(3,2));	
	printf("\n pow function : %.2f",pow(3.1,2.2));	
	
	printf("\n abs function : %d",abs(-12));	
	getch();
}
